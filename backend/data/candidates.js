// Generate random candidate function
export const generateRandomCandidate = (existingIds) => {
  const randomName = sampleNames[Math.floor(Math.random() * sampleNames.length)];
  const randomParty = parties[Math.floor(Math.random() * parties.length)];
  const randomDescription = sampleDescriptions[Math.floor(Math.random() * sampleDescriptions.length)];
  const randomImage = sampleImages[Math.floor(Math.random() * sampleImages.length)];
  
  // Generate unique ID
  const newId = Math.max(...existingIds, 0) + 1;
  
  return {
    id: newId,
    name: randomName,
    party: randomParty,
    description: randomDescription,
    image: randomImage
  };
};

// Calculate party statistics
export const getPartyStats = (candidates) => {
  const stats = {};
  parties.forEach(party => {
    stats[party] = 0;
  });
  
  candidates.forEach(candidate => {
    if (stats[candidate.party] !== undefined) {
      stats[candidate.party]++;
    }
  });
  
  return stats;
}; 